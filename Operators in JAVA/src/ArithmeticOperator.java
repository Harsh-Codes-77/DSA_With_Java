import java.util.*;

public class ArithmeticOperator{
    public static void main(String args[]){
        int A = 40;
        int B = 20;
        System.out.println(A+B);
        System.out.println(A-B);
        System.out.println(A*B);
        System.out.println((A/B));
        System.out.println(A%B);
    }
}